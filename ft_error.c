#include <unistd.h>

void	ft_error(void)
{
	write(1, "D", 1);
	write(1, "i", 1);
	write(1, "c", 1);
	write(1, "t", 1);
	write(1, " ", 1);
	write(1, "E", 1);
	write(1, "r", 1);
	write(1, "r", 1);
	write(1, "o", 1);
	write(1, "r", 1);
	write(1, "\n", 1);
}
